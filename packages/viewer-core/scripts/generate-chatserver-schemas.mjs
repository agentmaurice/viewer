const runtimeSchemaPath = path.join(chatserverRecipesDir, 'app_runtime_schema.go')
const uiSchemaPath = path.join(chatserverRecipesDir, 'ui_schema.go')
const outputPath = path.join(packageRoot, 'src', 'generated', 'chatserver-schemas.ts')

function extractJSONConst(goSource, constName) {
  const marker = `const ${constName} = \``
  const start = goSource.indexOf(marker)
  if (start === -1) {
    throw new Error(`Could not find ${constName} in Go source`)
  }

  const valueStart = start + marker.length
  const valueEnd = goSource.indexOf('`', valueStart)
  if (valueEnd === -1) {
    throw new Error(`Could not find closing backtick for ${constName}`)
  }

  return goSource.slice(valueStart, valueEnd)
}

function uniqueStringArray(values) {
  return [...new Set(values.filter((value) => typeof value === 'string' && value.length > 0))]
}

if (!existsSync(runtimeSchemaPath) || !existsSync(uiSchemaPath)) {
  if (existsSync(outputPath)) {
    console.warn(
      '[viewer-core] chatserver schemas not found, keeping existing generated/chatserver-schemas.ts',
    )
    process.exit(0)
  }

  throw new Error(
    `Could not locate chatserver schemas at ${runtimeSchemaPath} and ${uiSchemaPath}, and no pre-generated output exists at ${outputPath}`,
  )
}

const runtimeSchemaSource = readFileSync(runtimeSchemaPath, 'utf8')
const uiSchemaSource = readFileSync(uiSchemaPath, 'utf8')
const runtimeSchema = JSON.parse(extractJSONConst(runtimeSchemaSource, 'appRuntimeSchemaJSON'))
const uiSchema = JSON.parse(extractJSONConst(uiSchemaSource, 'appUISchemaJSON'))

const runtimeContract = runtimeSchema?.properties?.contract?.const
if (typeof runtimeContract !== 'string' || runtimeContract.length === 0) {
  throw new Error('Runtime schema contract const is missing')
}

const appInstanceStatuses = uniqueStringArray(runtimeSchema?.properties?.status?.enum ?? [])
if (appInstanceStatuses.length === 0) {
  throw new Error('Runtime schema status enum is missing')
}

const uiDefs = uiSchema?.$defs ?? {}
const appUINodeTypes = uniqueStringArray(
  Object.values(uiDefs).flatMap((definition) => {
    if (definition && typeof definition === 'object' && definition.properties && typeof definition.properties === 'object') {
      const typeValue = definition.properties.type?.const
      return typeof typeValue === 'string' ? [typeValue] : []
    }
    return []
  }),
)

const appUILayouts = uniqueStringArray([
  ...(uiDefs.sectionNode?.properties?.layout?.enum ?? []),
  ...(uiDefs.cardNode?.properties?.layout?.enum ?? []),
  ...(uiDefs.groupNode?.properties?.layout?.enum ?? []),
])

const appUICalloutTones = uniqueStringArray(uiDefs.calloutNode?.properties?.tone?.enum ?? [])

